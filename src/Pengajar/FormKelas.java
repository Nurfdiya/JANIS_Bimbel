    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO kelas VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText()); // id_kelas
            pst.setString(2, jTextField2.getText()); // no_ruangan
            pst.setString(3, jComboBox1.getSelectedItem().toString()); // jenjang
            pst.setString(4, jComboBox2.getSelectedItem().toString()); // hari
            pst.setString(5, jComboBox3.getSelectedItem().toString()); // id_pengajar
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data disimpan");
            clearForm();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

