    private void loadTable() {
        model = new DefaultTableModel();
        model.addColumn("ID Kelas");
        model.addColumn("No Ruangan");
        model.addColumn("Jenjang");
        model.addColumn("Hari");
        model.addColumn("ID Pengajar");
        try {
            pst = conn.prepareStatement("SELECT * FROM kelas");
            rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_kelas"),
                    rs.getString("no_ruangan"),
                    rs.getString("jenjang"),
                    rs.getString("hari"),
                    rs.getString("id_pengajar"),
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load: " + e);
        }
    }
    
    private void loadCombopengajar() {
        try (Connection conn = ConnectionDataBase.DBConnection.getConnection();
             Statement st = conn.createStatement();
             ResultSet rs = st.executeQuery("SELECT id_pengajar FROM pengajar")) {

            jComboBox3.removeAllItems();
            while (rs.next()) {
                jComboBox3.addItem(rs.getString("id_pengajar"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load pengajar: " + e.getMessage());
        }
    }

    
