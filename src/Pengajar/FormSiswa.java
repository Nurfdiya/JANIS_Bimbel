    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        jTextField1.setText(jTable1.getValueAt(row, 0).toString());
        jComboBox1.setSelectedItem(jTable1.getValueAt(row, 1).toString());
        jComboBox2.setSelectedItem(jTable1.getValueAt(row, 2).toString());

        try {
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd")
                    .parse(jTable1.getValueAt(row, 3).toString());
            jDateChooser1.setDate(date);
        } catch (Exception e) {
            e.printStackTrace();
        }

        jComboBox3.setSelectedItem(jTable1.getValueAt(row, 4).toString());
    }//GEN-LAST:event_jTable1MouseClicked

