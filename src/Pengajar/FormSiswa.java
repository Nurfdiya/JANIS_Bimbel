    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO siswa VALUES (?, ?, ?, ?, ?)";
        try (Connection con = ConnectionDataBase.DBConnection.getConnection();
             PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setString(1, jTextField1.getText());
            ps.setString(2, jComboBox1.getSelectedItem().toString());
            ps.setString(3, jComboBox2.getSelectedItem().toString());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            ps.setString(4, sdf.format(jDateChooser1.getDate()));

            ps.setString(5, jComboBox3.getSelectedItem().toString());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            loadTable();
            clearForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

