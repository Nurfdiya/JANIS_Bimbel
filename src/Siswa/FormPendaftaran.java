    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField5.getText().trim();

    if (keyword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan NISN yang ingin dicari.");
        return;
    }

    DefaultTableModel model = new DefaultTableModel(new Object[]{"NISN", "Nama Siswa", "Jenjang"}, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
try (Connection conn = ConnectionDataBase.DBConnection.getConnection();
         PreparedStatement stmt = conn.prepareStatement(
                 "SELECT nisn, nama_siswa, tingkat_pendidikan FROM pendaftaran WHERE nisn LIKE ?")) {

        stmt.setString(1, "%" + keyword + "%");
        ResultSet rs = stmt.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                    rs.getInt("nisn"),
                    rs.getString("nama_siswa"),
                    rs.getString("tingkat_pendidikan")
            });
        }

        jTable2.setModel(model);

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "User tidak ditemukan.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mencari: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        
        String nisn = jTable2.getValueAt(row,0).toString();
        String namaSiswa = jTable2.getValueAt(row,1).toString();
        String jenjang = jTable2.getValueAt(row,2).toString();
        String jenisKelamin = jTable2.getValueAt(row,3).toString();
        String noHP = jTable2.getValueAt(row,4).toString();
        String alamat = jTable2.getValueAt(row,5).toString();
        
        jTextField1.setText(nisn);
        jTextField2.setText(namaSiswa);
        jComboBox1.setSelectedItem(jenjang);
        if (jenisKelamin.equalsIgnoreCase("Laki-laki")) {
        jRadioButton1.setSelected(true);
        } else if (jenisKelamin.equalsIgnoreCase("Perempuan")) {
        jRadioButton2.setSelected(true);
        jTextField3.setText(noHP);
        jTextField4.setText(alamat);
}

    }//GEN-LAST:event_jTable2MouseClicked

