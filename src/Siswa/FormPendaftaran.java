    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String gender = jRadioButton1.isSelected() ? "Perempuan" : jRadioButton2.isSelected() ? "Laki-laki" : "";
            pst = conn.prepareStatement("INSERT INTO pendaftaran VALUES (?, ?, ?, ?, ?, ?)");
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jComboBox1.getSelectedItem().toString());
            pst.setString(4, gender);
            pst.setString(5, jTextField3.getText());
            pst.setString(6, jTextField4.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data disimpan.");
            clearForm();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal simpan: " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

